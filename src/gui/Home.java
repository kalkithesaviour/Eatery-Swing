    public void getAllTableNames() {
        try {
            ResultSet rs = db.DbConnect.st.executeQuery("select * from table_number");
            while (rs.next()) {
                String n = rs.getString("table_name");
                tables.addItem(n);
                tables2.addItem(n);
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    public void getAllDishNames() {
        try {
            ResultSet rs = db.DbConnect.st.executeQuery("select * from dishes");
            while (rs.next()) {
                String n = rs.getString("dish_name");
                int p = rs.getInt("price");
                dishes.addItem(n + " : " + p);
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    public void setAmount(String tnOfBill) {
        javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        int rc = dtm.getRowCount();
        int total = 0;
        for (int x = 0; x < rc; x++) {
            String t = (String) jTable1.getValueAt(x, 0);
            if (t.equalsIgnoreCase(tnOfBill)) {
                int p = (int) jTable1.getValueAt(x, 2);
                total += p;
            }
        }
        amt.setText(total + "");
    }
    
    public void compareTnOfBillWithTnOfTabelModel(String tn) {
        String tnOfBill = (String) tables2.getSelectedItem();
        if (tnOfBill.equalsIgnoreCase(tn)) {
            setAmount(tnOfBill);
        }
    }

